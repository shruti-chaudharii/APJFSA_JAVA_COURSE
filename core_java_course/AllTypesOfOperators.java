package java_programs;

public class AllTypesOfOperators {
public static void main(String[] args) {
		
		/* This is program for to demonstrate all the datatypes */
		
		int intVar = 30;   // declare integer data type
		
		short stortVar = 4;  // declare short datatype
		
		long longVar = 59;   // declare long variable
		
	    float floatVar = 17.8F;   // declare floating data type
	    
		byte byteVar = 8;   // declare byte data type 
		
		double doubleVar = 35.287;  // declare double data type
		
		boolean boolVar = false;  // declare boolean variable
		
		char charVar = 'V';   // declare character data type
		
		String strVar = "Shizuka";  // declare String variable
		
		
		
		/*Printing all the variable values*/
		
		
		System.out.println("Integer : "+intVar);
		System.out.println("Short : "+stortVar);
		System.out.println("Long : "+longVar);
		System.out.println("Float : "+floatVar);
		System.out.println("Byte : "+byteVar);
		System.out.println("Double : "+doubleVar);
		System.out.println("Boolean : "+boolVar);
		System.out.println("Character : "+charVar);
		System.out.println("String : "+strVar);	

	}

}


